ORG 100H

MOV AX, 1
MOV CX, 20

MOV DX, AX

L1:
	call print
	ADD AX, 1
LOOP L1

MOV AX, 4C00H
INT 21H

print:
	PUSH AX
	PUSH DX
	PUSH CX
	
	MOV BX, 10
	XOR CX, CX	
	MOV DX, 0

.REPEAT_DIVISION_AND_STORE_IN_STACK
	XOR DX, DX
	DIV BX
	PUSH DX
	INC CX
	CMP AX, 0
	JNZ .REPEAT_DIVISION_AND_STORE_IN_STACK

.PRINT_DIVISION_AND_STORE_IN_STACK
	POP DX
	ADD DL, '0'
	MOV AH, 02H
	INT 21H
LOOP .PRINT_DIVISION_AND_STORE_IN_STACK
	
	POP CX
	POP DX
	POP AX
ret
